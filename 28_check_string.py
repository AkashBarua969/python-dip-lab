# Check String
# You can use the 'in' keyword to check if a phrase or character is present in a string.

txt = "Attachment is the root of suffering."

# Check if "suffering" is present in the text
print("suffering" in txt)  # Output: True

# Check if NOT
# You can use the 'not in' keyword to check if a phrase is NOT present in the string.

# Check if "freedom" is NOT present in the text
print("freedom" not in txt)  # Output: True
