# Multiline Strings
# You can assign a multiline string to a variable by using triple quotes.

# Example using triple double quotes
a = """Peace comes from within. Do not seek it without.
What you think, you become.
Health is the greatest gift, contentment the greatest wealth.
The mind is everything. What you think you become."""
print(a)

# Example using triple single quotes
a = '''Peace comes from within. Do not seek it without.
What you think, you become.
Health is the greatest gift, contentment the greatest wealth.
The mind is everything. What you think you become.'''
print(a)

# Note: Line breaks are inserted at the same positions as in the code.
