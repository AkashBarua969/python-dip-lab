# Strings are Arrays
# In Python, strings are arrays of Unicode characters.
# A single character is simply a string of length 1.

# Example: Accessing character at a specific index
a = "Akash, Bhaiya!"
print(a[1])  # Output: 'k'

# Explanation:
# Index:     01234567890123
# String:   "A k a s h ,   B h a i y a !"
# The character at index 1 is 'k'
