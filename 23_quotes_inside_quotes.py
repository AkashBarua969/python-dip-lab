# Demonstrating how to use quotes inside strings in Python

# Using double quotes to wrap a string that contains a single quote
print("It's alright")  
# Output: It's alright

# Using double quotes to include single quotes inside the string
print("He is called 'Johnny'")  
# Output: He is called 'Johnny'

# Using single quotes to include double quotes inside the string
print('He is called "Johnny"')  
# Output: He is called "Johnny"
