# Check with if statement: 'in'
txt = "Life is suffering; desire leads to suffering."

# Check if "suffering" is present in the string
if "suffering" in txt:
    print("Yes, 'suffering' is present.")  # Output: Yes, 'suffering' is present.

# Check with if statement: 'not in'
# Check if "peace" is NOT present in the string
if "peace" not in txt:
    print("No, 'peace' is NOT present.")  # Output: No, 'peace' is NOT present.
